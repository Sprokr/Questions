class XOR
{
    public static void main(String args[])
    {
        int a = 3 ^ 5;
        System.out.println(a);
        System.out.println(a ^ 15);

        int b = 4 ^ 2;
        System.out.println(b);
        System.out.println(b ^ 8);

        int c = 0 ^ 3;
        System.out.println(c);
    }
}
